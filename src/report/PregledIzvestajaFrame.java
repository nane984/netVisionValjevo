    private void fillTableIzvestaj(JTable table) {

        ukupnoAgregat04 = 0;
        ukupnoAgregat48 = 0;
        ukupnoAgregat816 = 0;
        ukupnoAgregat1632 = 0;
        ukupnoCement = 0;
        ukupnoFiler = 0;
        ukupnoAditiv1 = 0;
        ukupnoAditiv2 = 0;
        ukupnoVoda = 0;
        ukupnoBetona = 0;

        int brRow = table.getRowCount();
        
        Object[][] obj = new Object[brRow+1][13];
        for (int j = 0; j < brRow; j++) {
            getRowsukupno(obj, j, table);
        }
        
        obj[brRow][0] = brRow + 1;
        obj[brRow][1] = "ukupno";
        obj[brRow][2] = ukupnoAgregat04;
        obj[brRow][3] = ukupnoAgregat48;
        obj[brRow][4] = ukupnoAgregat816;
        obj[brRow][5] = ukupnoAgregat1632;
        obj[brRow][6] = ukupnoCement;
        obj[brRow][7] = ukupnoFiler;
        obj[brRow][8] = Convert.getRounFloat(ukupnoAditiv1, 2);
        obj[brRow][9] = Convert.getRounFloat(ukupnoAditiv2, 2);
        obj[brRow][10] = ukupnoVoda;
        obj[brRow][11] = Convert.round2Decimal(ukupnoBetona);
        obj[brRow][12] = "";
       
       

        DefaultTableModel model = new DefaultTableModel(obj, new Object[]{"r.b", "rn", "0_4 izd", "4_8 izd", "8_16 izd", "16_32 izd", "cement", "filer", "aditiv 1", "aditiv 2", "voda", "izdato m3","ime betona"});
        jTableIzvestaj.setModel(model);
        jTableIzvestaj.setRowMargin(4);
        jTableIzvestaj.setRowHeight(40);
        jTableIzvestaj.setShowGrid(true);
        jTableIzvestaj.getColumnModel().getColumn(0).setMaxWidth(45);
        jTableIzvestaj.setDefaultEditor(Object.class, null);
        
        

    }

    private void getRowsukupno(Object[][] obj, int j, JTable table) {
        List<db.Sarza> result;

        long ukupnoRn = 0l;
        int ukupnoAgregatizdato04 = 0;
        int ukupnoAgregatizdato48 = 0;
        int ukupnoAgregatizdato816 = 0;
        int ukupnoAgregatizdato1632 = 0;
        int ukupnoCementizdato = 0;
        int ukupnoFilerizdato = 0;
        float ukupnoAditiv1izdato = 0;
        float ukupnoAditiv2izdato = 0;
        int ukupnoVodaIzdata = 0;
        float ukupnoBetonaIzdato = 0;
        String imeBetona =""; 

        //int i = 0;
        result = sarzaDb.getSarzeZaRadniNalog(Long.parseLong(table.getValueAt(j, 1).toString()));

        for (db.Sarza auto : result) {
            ukupnoRn = auto.getRadninalog().getRn();
            ukupnoAgregatizdato04 = ukupnoAgregatizdato04 + auto.getAgregatizdato04();
            ukupnoAgregatizdato48 = ukupnoAgregatizdato48 + auto.getAgregaizdato48();
            ukupnoAgregatizdato816 = ukupnoAgregatizdato816 + auto.getAgregatizdato816();
            ukupnoAgregatizdato1632 = ukupnoAgregatizdato1632 + auto.getAgregatizdato1632();
            ukupnoCementizdato = ukupnoCementizdato + auto.getCementizdato();
            ukupnoFilerizdato = ukupnoFilerizdato + auto.getFilerizdato();
            ukupnoAditiv1izdato = ukupnoAditiv1izdato + auto.getAditiv1izdato();
            ukupnoAditiv2izdato = ukupnoAditiv2izdato + auto.getAditiv2izdato();
            ukupnoVodaIzdata = (int) (ukupnoVodaIzdata + auto.getVodaizdato());
            ukupnoBetonaIzdato = auto.getRadninalog().getIsporucenom3();
            imeBetona = auto.getRadninalog().getMarkabetona();
        }
        
        ukupnoAgregat04 = ukupnoAgregat04 + ukupnoAgregatizdato04;
        ukupnoAgregat48 = ukupnoAgregat48 + ukupnoAgregatizdato48;
        ukupnoAgregat816 = ukupnoAgregat816 + ukupnoAgregatizdato816;
        ukupnoAgregat1632 = ukupnoAgregat1632+ ukupnoAgregatizdato1632;
        ukupnoCement = ukupnoCement + ukupnoCementizdato;
        ukupnoFiler = ukupnoFiler + ukupnoFilerizdato;
        ukupnoAditiv1 = ukupnoAditiv1 + ukupnoAditiv1izdato;
        ukupnoAditiv2 = ukupnoAditiv2 + ukupnoAditiv2izdato;
        ukupnoVoda = ukupnoVoda + ukupnoVodaIzdata;
        ukupnoBetona = ukupnoBetona + ukupnoBetonaIzdato;

        obj[j][0] = j + 1;
        obj[j][1] = ukupnoRn;
        obj[j][2] = ukupnoAgregatizdato04;
        obj[j][3] = ukupnoAgregatizdato48;
        obj[j][4] = ukupnoAgregatizdato816;
        obj[j][5] = ukupnoAgregatizdato1632;
        obj[j][6] = ukupnoCementizdato;
        obj[j][7] = ukupnoFilerizdato;
        obj[j][8] = Convert.getRounFloat(ukupnoAditiv1izdato, 2);
        obj[j][9] = Convert.getRounFloat(ukupnoAditiv2izdato, 2);
        obj[j][10] = ukupnoVodaIzdata;
        obj[j][11] = Convert.round2Decimal(ukupnoBetonaIzdato);
        obj[j][12] = imeBetona;
        
    }


