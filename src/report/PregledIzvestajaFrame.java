     private void printReport(JTable jTable) {
        try {
            DefaultTableModel de = (DefaultTableModel) jTable.getModel();
            JasperReport jr = JasperCompileManager.compileReport("C:\\NetVision\\reports\\Report1.jrxml");
            //JasperReport jr = JasperCompileManager.compileReport("D:\\Programiranje\\netVisionValjevo\\src\\reports\\Report1.jrxml");

            
            
            JasperPrint jasperPrint = JasperFillManager.fillReport(jr, podaci, new JRTableModelDataSource(de));
            JasperViewer.viewReport(jasperPrint, false);

        } catch (JRException ex) {
            Logger.getLogger(PregledIzvestajaFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void setPodaciZaglavlje(){
        podaci = new HashMap();
        podaci.put("rn", "");
        podaci.put("markaBetona", "");
        podaci.put("kupac", "");
        podaci.put("gradiliste", "");
        podaci.put("datumOd", "");
        podaci.put("datumDo", "");
        
        
        if (brRadnohNaloga.getText().length() != 0) {
            podaci.put("rn", brRadnohNaloga.getText());
        }
        if (MarkaBetona.getText().length() != 0) {
            podaci.put("markaBetona", MarkaBetona.getText());
        }
        if (Kupac.getText().length() != 0) {
            podaci.put("kupac", Kupac.getText());
        }
        
        if (Gradiliste.getText().length() != 0) {
            podaci.put("gradiliste", Gradiliste.getText());
        }

        if (DatumOd.getText().length() != 0) {
            podaci.put("datumOd", DatumOd.getText());
        }

       if (DatumDo.getText().length() != 0) {
            podaci.put("datumDo", DatumDo.getText());
        }
        
    }
    
